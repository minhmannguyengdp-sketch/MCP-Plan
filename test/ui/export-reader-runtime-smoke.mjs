async function waitForHttp(url, timeoutMs = 120_000) {
  const started = Date.now();
  let lastError = null;
  while (Date.now() - started < timeoutMs) {
    try {
      const response = await fetch(url, { cache: "no-store" });
      if (response.ok) return;
      lastError = new Error(`${url} -> ${response.status}`);
    } catch (error) {
      lastError = error;
    }
    await new Promise((resolve) => setTimeout(resolve, 500));
  }
  throw lastError || new Error(`timeout_waiting_for_${url}`);
}

async function get(path) {
  const response = await fetch(`${appBase}${path}`, { cache: "no-store", headers: { Accept: "*/*" } });
  const body = await response.text();
  return {
    status: response.status,
    contentType: response.headers.get("content-type") || "",
    disposition: response.headers.get("content-disposition") || "",
    body
  };
}

async function getBinary(path) {
  const response = await fetch(`${appBase}${path}`, { cache: "no-store", headers: { Accept: "*/*" } });
  const bytes = new Uint8Array(await response.arrayBuffer());
  return {
    status: response.status,
    contentType: response.headers.get("content-type") || "",
    disposition: response.headers.get("content-disposition") || "",
    bytes
  };
}

function rejectsTechnicalCopy(body, label) {
  assert.doesNotMatch(body, /Dashboard quản trị|BC phiên|XUAT PHIEN|Checklist|Top SP\/brand|Next action|Generated by|Session ID|Snapshot|Schema version|Health|Cần truyền fileId/i, `${label} must not expose developer copy`);
}

